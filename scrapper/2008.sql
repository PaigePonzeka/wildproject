<p>The Wild Project is pleased to present its second exhibition, Vanitas, an installation of works on paper by
Joseph Hart. Fusing painting, drawing, and collage techniques, Hart focuses on the language of display. His
detailed renderings of museum vitrines and his delicate reinterpretations of classical floral still lifes examine
