heads and other artifacts sit atop sharp lines as if they were the proudly speared conquests of a primitive
ritual. In Funny Feeling, shards of color extend across an obsessively drawn backdrop of graphite mimicking
a botanical still life on the brink of extinction. Hart!s elegant images highlight the significance of viewing
history through artifact.</p>
<p>Originally from New Hampshire, Hart lives and works in Brooklyn, New York. He received a BFA from the
Rhode Island School of Design in 1999. Most recently, his work was featured in the New Talents section at
Art Cologne, and in Here And Elsewhere, an exhibition of emerging artists at The Bronx Museum of the Arts.
He also recently served as a jury member for the 2008 Scholastic Art Awards, a national scholarship
program for young artists. Hart\'s mixed media works on paper have been exhibited at Galerie Markus Winter
in Berlin, Galleri Loyal in Stockholm, Galeri Vidal St. Phalle in Paris, Klaus Von Nichtssagend in Brooklyn, as
well as Alexander &amp; Bonin, Freight + Volume and CRG Gallery in New York.</p>
<p>The Wild Project, a new environmentally conscious venue for contemporary theater, film, and visual arts, is
